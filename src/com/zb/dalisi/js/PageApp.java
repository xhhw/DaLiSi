package com.zb.dalisi.js;

public class PageApp {
	private String pageUrl;
	private String elementId;
	private String event;
	private String funcId;
	private String pageType;
	private String pageParent;
	private String funcName;
	private String fileName;
	private String jsSrcType;
	
	public String getPageUrl() {
		return pageUrl;
	}
	public void setPageUrl(String pageUrl) {
		this.pageUrl = pageUrl;
	}
	public String getElementId() {
		return elementId;
	}
	public void setElementId(String elementId) {
		this.elementId = elementId;
	}
	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}
	public String getFuncId() {
		return funcId;
	}
	public void setFuncId(String funcId) {
		this.funcId = funcId;
	}
	public String getPageType() {
		return pageType;
	}
	public void setPageType(String pageType) {
		this.pageType = pageType;
	}
	public String getPageParent() {
		return pageParent;
	}
	public void setPageParent(String pageParent) {
		this.pageParent = pageParent;
	}
	public String getFuncName() {
		return funcName;
	}
	public void setFuncName(String funcName) {
		this.funcName = funcName;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getJsSrcType() {
		return jsSrcType;
	}
	public void setJsSrcType(String jsSrcType) {
		this.jsSrcType = jsSrcType;
	}
	
}
